local isNowSitting = false
function sitRedux(pChairEntity, pChairModel)
    local playerPed = PlayerPedId()
    isNowSitting = false
    if HasPedFlag(playerPed, "isSittingOnChair") then
        if defaultExitScenario then
          blackjackAnimDictToLoad = "anim_casino_b@amb@casino@games@shared@player@"
          RequestAnimDict(blackjackAnimDictToLoad)
          while not HasAnimDictLoaded(blackjackAnimDictToLoad) do 
              Wait(0)
          end
          ClearPedTasksImmediately(playerPed)
          TaskPlayAnim(GetPlayerPed(-1), blackjackAnimDictToLoad, "sit_exit_left", 1.0, 1.0, 2500, 0)
        elseif IsPedUsingScenario(playerPed, "PROP_HUMAN_SEAT_CHAIR_MP_PLAYER") then
            TriggerEvent("animation:cancel")
            Wait(1200)
            if oldLocation ~= nil then
                SetEntityCoords(playerPed, oldLocation)
                oldLocation = nil
            end
        end
    else
        isNowSitting = true
        oldLocation = GetOffsetFromEntityInWorldCoords(playerPed, 0.0, 0.0, -1.0)
        defaultExitScenario = nil
        Citizen.CreateThread(function()
            while isNowSitting and DoesEntityExist(pChairEntity) do
                Wait(0)
                DisableCamCollisionForEntity(pChairEntity)
            end
        end)
        for _, chair in pairs(Config.Sitable) do
            if GetHashKey(chair.prop) == pChairModel or chair.prop == pChairModel then
                defaultExitScenario = chair.scenarioExit or nil
                existingChairEntity = pChairEntity
                chairConfig = chair
                chairOffsetPosition = GetOffsetFromEntityInWorldCoords(
                    pChairEntity,
                    chair.leftOffset + 0.0,
                    chair.forwardOffset + 0.0,
                    chair.verticalOffset + 1.0
                )
                if chairOffsetPosition == vector3(0, 0, 0) then
                    isNowSitting = false
                    oldLocation = nil
                    return
                end
                headingOffset = chair.headingOffset ~= nil and chair.headingOffset or 180.0
                TaskStartScenarioAtPosition(
                    playerPed,
                    chair.scenario,
                    chairOffsetPosition.x + 0.0,
                    chairOffsetPosition.y + 0.0,
                    chairOffsetPosition.z + 0.0,
                    GetEntityHeading(pChairEntity) + headingOffset + 0.0,
                    0,
                    true,
                    true
                )
                break
            end
        end
    end
    SetPedFlag(playerPed, "isSittingOnChair", isNowSitting)
    SetEntityInvincible(pChairEntity, isNowSitting)
end


